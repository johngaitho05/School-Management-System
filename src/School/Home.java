    private void jButton12ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton12ActionPerformed
        // TODO add your handling code here:
        String adm, fn, mn,ln,id,pn,gn,query, query2;
        adm=admno.getText();
        fn= fname.getText();
        mn=mname.getText();
        ln=lname.getText();
        id= idno.getText();
        pn=pno.getText();
        query2= "select * from students where Adm=?";
        query= "insert into students values(?,?,?,?,?,?,?)";
        if (rbmale.isSelected()){
            gn= "Male";
        }else{
            gn= "Female";
        }
        try{
            Connection connection=DriverManager.getConnection("jdbc:mysql://localhost:3306/school","root","");
            PreparedStatement st=connection.prepareStatement(query2);
            st.setString(1,admno.getText());
            ResultSet rs= st.executeQuery();
            if (rs.next()){
                hoverbar_st.setForeground(Color.red);
                hoverbar_st.setText("That admission number is already taken");                
            }else{
            
            PreparedStatement pst=connection.prepareStatement(query);
            pst.setString(1, adm);
            pst.setString(2, fn);
            pst.setString(3, mn);
            pst.setString(4, ln);
            pst.setString(5, id);
            pst.setString(6, pn);
            pst.setString(7, gn);
            int data= pst.executeUpdate();
            if (data>0){
                hoverbar_st.setForeground(Color.green);
                hoverbar_st.setText("One student successfully registered");
            }else{
                hoverbar_st.setForeground(Color.red);
                hoverbar_st.setText("Registration failed");
            }
            }
        }catch (SQLException ex) {
            hoverbar_st.setForeground(Color.red);
            hoverbar_st.setText("Registration failed-sqlError");
        }
        student_ok.setVisible(true);
        
    }//GEN-LAST:event_jButton12ActionPerformed

