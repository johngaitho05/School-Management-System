    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        // TODO add your handling code here:
        String d,query,t;
        d=description.getText();
        t=Title.getText();
        query= "insert into events values(?,?,?,?)";
        Date utilDate = jDateChooser_in1.getDate();
        java.sql.Date sqlDate = new java.sql.Date(utilDate.getTime());
        Date utilDate2 = jDateChooser_out1.getDate();
        java.sql.Date sqlDate2 = new java.sql.Date(utilDate2.getTime());

        try {
            Connection connection = DriverManager.getConnection("jdbc:mysql://localhost:3306/school","root","");
            PreparedStatement pst=connection.prepareStatement(query);
            pst.setDate(1, sqlDate);
            pst.setDate(2, sqlDate2);
            pst.setString(3, t);
            pst.setString(4, d);
            int data =pst.executeUpdate();
            if (data>0){
                events_ok.setVisible(true);
                hover_events.setVisible(true);
                hover_events.setForeground(Color.green);
                hover_events.setText("Event successfully saved");
            }else{
                hover_events.setForeground(Color.red);
                hover_events.setText("Failed to save event");
            }

        }catch (SQLException ex) {
            events_ok.setVisible(true);
            hover_events.setVisible(true);
            hover_events.setForeground(Color.red);
            hover_events.setText("Failed to save event");
        }

    }//GEN-LAST:event_saveActionPerformed

